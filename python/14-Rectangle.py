"""Rectangle"""
height = int(input())
width = int(input())
def area_of_the_rectangle(h,w) :
    """area_of_the_rectangle"""
    print(w*h)

def length_of_the_rectangle(h,w) :
    """length_of_the_rectangle"""
    print(2*(w+h))
area_of_the_rectangle(height,width)
length_of_the_rectangle(height,width)
